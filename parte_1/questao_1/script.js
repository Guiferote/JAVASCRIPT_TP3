
/*
Operadores simples:
"==", "!=" são aqueles que comparam se dois valores são iguais, mas fazem "vista-grossa" 
para tipos diferentes.

A expressão ('5' == 5 ) por exemplo teria como resultado true, mesmo que esteja comparando uma string com um number
*/

/*
Operadores estritos:
"===", "!==" são aqueles que comparam se dois valores são estritamente iguais, sem margem para diferença de tipos
diferente do exemplo anterior, apenas a expressão a seguir seria true (5 === 5)
*/